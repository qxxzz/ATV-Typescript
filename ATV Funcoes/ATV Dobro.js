/**
 * Duplicador Numérico Simples
 * Esta função foi desenvolvida para calcular o dobro de um valor.
 * Ela recebe um único número como entrada e retorna seu valor multiplicado por dois.
 */
function dobro(n) {
    // Retorna o resultado da multiplicação do número de entrada por 2.
    return n * 2;
}
// Invoca a função 'dobro' com um número específico e exibe o resultado formatado.
console.log("Dobro de 12 = ".concat(dobro(12)));