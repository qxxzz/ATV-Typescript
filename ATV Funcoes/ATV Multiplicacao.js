/**
 * Operação de Produto Numérico
 * Esta função é projetada para realizar a multiplicação de dois números.
 * Ela recebe dois valores como entrada e retorna o seu produto.
 */
function multiplicar(a, b) {
    // Retorna o produto de 'a' e 'b'.
    return a * b;
}
// Invoca a função 'multiplicar' com dois valores e exibe o resultado formatado no console.
console.log("Resultado: ".concat(multiplicar(6, 7)));