/**
 * Função de Diferença Numérica
 * Recebe dois números como entrada e calcula a diferença entre eles.
 * O valor resultante da subtração é então devolvido.
 */
function subtrair(a, b) {
    // Executa a operação de subtração entre 'a' e 'b'.
    return a - b;
}
// Chama a função 'subtrair' com argumentos específicos e exibe o resultado formatado.
console.log("Resultado: ".concat(subtrair(20, 8)));