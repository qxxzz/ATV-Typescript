/**
 * Realiza Multiplicação de Entradas
 * Define uma função que aceita dois parâmetros numéricos.
 * Seu propósito é calcular o produto desses dois números e retorná-lo.
 */
function multiplicar(a: number, b: number): number {
  // Retorna o valor obtido da multiplicação de 'a' por 'b'.
  return a * b;
}

// Demonstra a execução da função 'multiplicar' e mostra o resultado.
console.log(`Resultado: ${multiplicar(6, 7)}`);