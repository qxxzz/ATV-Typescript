/**
 * Função de Saudação Personalizada
 * Exibe uma mensagem de cumprimento customizada com o nome fornecido
 */
function dizerOla(nome) {
    // Apresenta saudação personalizada no console
    console.log("Ol\u00E1, ".concat(nome, "!"));
}
// Chamadas da função com diferentes nomes
dizerOla("Eduardo");
dizerOla("Pietro");