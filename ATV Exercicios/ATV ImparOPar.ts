/**
 * Verifica se um número é par ou ímpar e exibe o resultado.
 */
let numero: number = 37; // número a ser verificado (pode alterar para testar)

// Verifica se o número é divisível por 2
if (numero % 2 === 0) {
  console.log(`${numero} é PAR.`); // número par
} else {
  console.log(`${numero} é ÍMPAR.`); // número ímpar
}
