// Define o preço inicial da camiseta
var camiseta1 = 80;

// Define a porcentagem de desconto (15%)
var desconto1 = 0.15;

// Calcula quanto será descontado do valor original
var valorDesconto1 = camiseta1 * desconto1;

// Subtrai o desconto do valor original para obter o preço final
var precoFinal1 = camiseta1 - valorDesconto1;

// Mostra no console os valores calculados
console.log("Preço original: R$", camiseta1);
console.log("Desconto: R$", valorDesconto1);
console.log("Preço final com desconto: R$", precoFinal1);
