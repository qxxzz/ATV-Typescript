/**
 * Avaliação de Resultado Acadêmico
 * Verifica se a nota obtida atinge o mínimo necessário para aprovação
 */
var nota = 5;

// Comparação com a nota de corte
if (nota >= 6) {
    // Nota igual ou superior ao requisito mínimo
    console.log("Aluno aprovado.");
}
else {
    // Nota abaixo do padrão exigido
    console.log("Aluno reprovado.");
}