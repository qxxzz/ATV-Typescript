/**
 * Verificação de Valor Numérico
 * Analisa se um valor é maior ou igual a zero ou se está abaixo de zero.
 */
var numero = -5;
if (numero >= 0) {
    // Valor não negativo encontrado
    console.log("".concat(numero, " \u00E9 positivo."));
}
else {
    // Valor negativo detectado
    console.log("".concat(numero, " \u00E9 negativo."));
}