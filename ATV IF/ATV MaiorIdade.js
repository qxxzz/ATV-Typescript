/**
 * Verificação de Maioridade Legal
 * Determina se uma pessoa atingiu a idade legal adulta
 */
var idade = 17;

// Comparação com a idade mínima legal
if (idade >= 18) {
    // Idade igual ou superior ao requisito legal
    console.log("Maior de idade.");
}
else {
    // Idade abaixo do limite estabelecido
    console.log("Menor de idade.");
}